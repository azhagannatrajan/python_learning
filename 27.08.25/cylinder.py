radius = 10
height = 20
pi = 3.14

#Volume of the Cylinder
volume = pi*radius**2*height

#print the volume
print("The Volume of the Cylinder is:", volume)

#surface area of the cylinder
surface = 2*pi*radius*(radius+height)

#print the surface area
print("The Total Surface area fo the cylinder is:", surface)

#volume of the sphere
sphere = 4/3*pi*radius**3

#print the volume pf sphere
print("The volume of the sphere is:", sphere)

#finding the data type
print(type(sphere))
print(type(surface))
print(type(volume))
